/// <summary>
/// verifica daca doua variabile de tip double sunt egale
/// </summary>
/// <param name="a">double
/// <param name="b">double
/// <returns> rez
/// rez = 0: a != b
/// rez = 1: a == b
int double_equ(double a, double b);

/// <summary>
/// verifica daca doi pointeri de caractere sunt egali
/// </summary>
/// <param name="a">pointer de caractere
/// <param name="b">pointeri de caractere
/// <returns> rez
/// rez = 0: a != b
/// rez = 1: a == b
int str_equ(char* a, char* b);
